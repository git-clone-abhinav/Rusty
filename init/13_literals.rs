fn main(){
    let one = 1;
    // datatype = integer
    // literal - method to write value to a variable

    let x:i8 = 10;
    // datatype = integer

    let y = 10i8;
    // datatype = integer

    let z = 10_i8;
    // datatype = integer

    let two = 2.0;
    // let two:f32 = 2.0;
    // let two = 2.0f32;
    // datatype = float

    let three = "3";
    // datatype = string

    let four = '4';
    // datatype = character

    let five = true;
    // datatype = boolean

    println!("{} {} {} {} {} {} {} {}", one, x, y, z, two, three, four, five);

}